# sequence type
# string - represents sequence of characters
s = "Hello, Balaji!"
print(type(s))

# List - represents an ordered collection of items - ordered and mutable(list can change)
l = [1, 2, 3, 4, 5]
print(type(l))

# Tuple - represents an ordered and immutable(elements cannot be changed, added or removed) collection of items
t = (1,2,3,4,5)
print(type(t))


# mapping
# dictionary - represents collection of key-value pair (mutable- add, update or remove elements)
d= {"name" : "Balaji", "age" : 24}
print(type(d))
